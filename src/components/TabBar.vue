<script setup lang="ts">
import { ref, computed, watch, onMounted, nextTick } from 'vue'
import { useTerminalStore } from '../stores/terminal'

const terminalStore = useTerminalStore()

// ÊãñÊãΩÁä∂ÊÄÅ
const dragIndex = ref<number | null>(null)
const dragOverIndex = ref<number | null>(null)

// Êñ∞Âª∫ÁªàÁ´Ø‰∏ãÊãâËèúÂçï
const showNewMenu = ref(false)
const menuPosition = ref({ top: '0px', left: '0px' })

// ÊªöÂä®Áõ∏ÂÖ≥
const tabsContainerRef = ref<HTMLElement | null>(null)
const canScrollLeft = ref(false)
const canScrollRight = ref(false)

// Ê£ÄÊµãÊìç‰ΩúÁ≥ªÁªü
const isWindows = computed(() => navigator.platform.toLowerCase().includes('win'))

// Shell ÈÄâÈ°π
const shellOptions = computed(() => {
  if (isWindows.value) {
    return [
      { label: 'PowerShell', value: 'powershell.exe', icon: '‚ö°' },
      { label: 'CMD', value: 'cmd.exe', icon: 'üìü' },
      { label: 'Git Bash', value: 'C:\\Program Files\\Git\\bin\\bash.exe', icon: 'üê±' }
    ]
  } else {
    return [
      { label: 'Bash', value: '/bin/bash', icon: 'üêö' },
      { label: 'Zsh', value: '/bin/zsh', icon: 'üîÆ' },
      { label: 'Fish', value: '/usr/bin/fish', icon: 'üêü' }
    ]
  }
})

// Ê£ÄÊü•ÊªöÂä®Áä∂ÊÄÅ
const checkScrollState = () => {
  const container = tabsContainerRef.value
  if (!container) return
  
  canScrollLeft.value = container.scrollLeft > 0
  canScrollRight.value = container.scrollLeft < container.scrollWidth - container.clientWidth - 1
}

// ÊªöÂä®Âà∞ÊåáÂÆöÊñπÂêë
const scroll = (direction: 'left' | 'right') => {
  const container = tabsContainerRef.value
  if (!container) return
  
  const scrollAmount = 200
  container.scrollBy({
    left: direction === 'left' ? -scrollAmount : scrollAmount,
    behavior: 'smooth'
  })
}

// ÊªöÂä®Âà∞ÂΩìÂâçÊøÄÊ¥ªÁöÑ tab
const scrollToActiveTab = () => {
  nextTick(() => {
    const container = tabsContainerRef.value
    if (!container) return
    
    const activeTab = container.querySelector('.tab.active') as HTMLElement
    if (!activeTab) return
    
    const containerRect = container.getBoundingClientRect()
    const tabRect = activeTab.getBoundingClientRect()
    
    // Â¶ÇÊûú tab ‰∏çÂú®ÂèØËßÅËåÉÂõ¥ÂÜÖÔºåÊªöÂä®Âà∞ÂèØËßÅ
    if (tabRect.left < containerRect.left) {
      container.scrollBy({
        left: tabRect.left - containerRect.left - 10,
        behavior: 'smooth'
      })
    } else if (tabRect.right > containerRect.right) {
      container.scrollBy({
        left: tabRect.right - containerRect.right + 10,
        behavior: 'smooth'
      })
    }
  })
}

// ÁõëÂê¨ tab ÂèòÂåñÂíåÊøÄÊ¥ªÁä∂ÊÄÅÂèòÂåñ
watch(() => terminalStore.tabs.length, () => {
  nextTick(checkScrollState)
})

watch(() => terminalStore.activeTabId, () => {
  scrollToActiveTab()
})

onMounted(() => {
  checkScrollState()
  // ÁõëÂê¨ÊªöÂä®‰∫ã‰ª∂
  tabsContainerRef.value?.addEventListener('scroll', checkScrollState)
  // ÁõëÂê¨Á™óÂè£Â§ßÂ∞èÂèòÂåñ
  window.addEventListener('resize', checkScrollState)
})

const handleNewTab = (shell?: string) => {
  terminalStore.createTab('local', undefined, shell)
  showNewMenu.value = false
}

const toggleNewMenu = (event: MouseEvent) => {
  if (!showNewMenu.value) {
    // ËÆ°ÁÆóËèúÂçï‰ΩçÁΩÆ
    const button = event.currentTarget as HTMLElement
    const rect = button.getBoundingClientRect()
    menuPosition.value = {
      top: `${rect.bottom + 4}px`,
      left: `${rect.right - 150}px`  // 150 ÊòØËèúÂçïÂÆΩÂ∫¶
    }
  }
  showNewMenu.value = !showNewMenu.value
}

const hideNewMenu = () => {
  showNewMenu.value = false
}

const handleCloseTab = (tabId: string, event: MouseEvent) => {
  event.stopPropagation()
  terminalStore.closeTab(tabId)
}

// ÊãñÊãΩÂºÄÂßã
const handleDragStart = (index: number, event: DragEvent) => {
  dragIndex.value = index
  if (event.dataTransfer) {
    event.dataTransfer.effectAllowed = 'move'
    event.dataTransfer.setData('text/plain', index.toString())
  }
}

// ÊãñÊãΩÁªèËøá
const handleDragOver = (index: number, event: DragEvent) => {
  event.preventDefault()
  if (event.dataTransfer) {
    event.dataTransfer.dropEffect = 'move'
  }
  dragOverIndex.value = index
}

// ÊãñÊãΩÁ¶ªÂºÄ
const handleDragLeave = () => {
  dragOverIndex.value = null
}

// ÊîæÁΩÆ
const handleDrop = (toIndex: number, event: DragEvent) => {
  event.preventDefault()
  if (dragIndex.value !== null && dragIndex.value !== toIndex) {
    terminalStore.reorderTabs(dragIndex.value, toIndex)
  }
  dragIndex.value = null
  dragOverIndex.value = null
}

// ÊãñÊãΩÁªìÊùü
const handleDragEnd = () => {
  dragIndex.value = null
  dragOverIndex.value = null
}
</script>

<template>
  <div class="tab-bar">
    <!-- Â∑¶ÊªöÂä®ÊåâÈíÆ -->
    <button 
      v-show="canScrollLeft" 
